                    <br>
                    C. &emsp;On packaging boxes, 
                    <br>
                    &emsp;•	Box must be made of corrugated paper board (preferably double wall). Always use the box on bulky and heavy items 5kg above.  
                    <br>
                    &emsp;•	High value items (amounting to a at least P2,500) should be placed in a carton box.
                    <br>
                    &emsp;•	Avoid reusing old carton boxes as they may have lost some of their rigidity. 
                    <br>
                    &emsp;•	For wipes, always use sturdy box for additional rigidness to avoid crashing the wipes.
                    <br>
                    &emsp;•	Products that are placed inside a small to medium sized boxes should be wrapped at least twice.
                    <br>
                    &emsp;•	All fragile items should be placed in a box. Fragile items with minimum weight of 20kgs should be crated.
                    <br>
                    D. &emsp;On packaging tapes,
                    <br>
                    &emsp;•	Use of clear packaging tape with logo/branding of merchant is allowed.
                    <br>
                    E. &emsp;On shrink wraps,
                    <br>
                    &emsp;•	Items should not be shipped in its original packaging. It should be protected with shrink wrap. 
                    <br>
                    &emsp;•	The caps of liquid items are required to be properly wrapped with tape or shrink wrap then secured with bubble wrap to avoid spillage and damage.
                    <br>
                    F. &emsp;On bubble wraps, 
                    <br>
                    &emsp;•	Garments, fabrics, cords, and other similar items should be wrapped in bubble wrap then placed in a pouch. 
                    <br>
                    &emsp;•	Odd-shaped items should be wrapped with the bubble wrap then placed in a box with fillers to prevent item from moving.
                    <br>
                    &emsp;•	Fragile items, liquid items including wipes and appliances, shall be packed with 2 layers or at least 5cm thickness of bubble wrap for each item from bottom to top then tighten the bubble wrap with tape. This is to avoid breakage, crashing or being punctured. 
                    <br>
                    &emsp;•	If the manufacturing box of the parcel is valuable, secure it with bubble wrap. 
                    <br>
                    &emsp;•	If the bulky item is secured with a corrugated box, no need to wrap the box with bubble wrap.
                    <br>
                    G. &emsp;On fillers and dividers,
                    <br>
                    &emsp;•	Fillers or Styrofoam cushion should be provided to protect the items and prevent vibration, impact or shock inside the box during transport.
                    <br>
                    H. &emsp;On different tracking numbers, 
                    <br>
                    &emsp;•	Orders that generated two tracking numbers should be packed separately.
                    <br>
                    I. &emsp;On envelope sleeve. 
                    <br>
                    &emsp;•	Shipping label should be placed in an envelope sleeve if item is not placed in a pouch. Pouch should not be used as an alternative for envelope sleeve.
